floor = Math.floor(1.5); // rounds down to the nearest integer
console.log(floor); // returns 1

trunc = Math.trunc(1.5); // returns the integer part of a number
console.log(trunc); // returns 1

round = Math.round(1.5); // rounds to the nearest integer
console.log(round); // returns 2

ceil = Math.ceil(1.5); // rounds up to the nearest integer
console.log(ceil); // returns 2
