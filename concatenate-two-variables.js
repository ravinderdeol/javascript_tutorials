// Addition Operator

const first_name = 'ravinder';
const last_name = 'deol';

const full_name = first_name + ' ' + last_name;

console.log(full_name);

// Template Literals

const first_name = 'ravinder';
const last_name = 'deol';

const full_name = `${first_name} ${last_name}`;

console.log(full_name);

// Concat Method

const first_name = 'ravinder';
const last_name = 'deol';

const full_name = first_name.concat(' ', last_name);

console.log(full_name);

// Join Method

const first_name = 'ravinder';
const last_name = 'deol';

const full_name = [first_name, last_name].join(' ');

console.log(full_name);
